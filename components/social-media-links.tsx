const DEFAULT_HREFS = {
  x: `https://x.com/${SOCIAL_HANDLE}`,
  facebook: `https://www.facebook.com/${SOCIAL_HANDLE}`,
  instagram: `https://www.instagram.com/${SOCIAL_HANDLE}/`,
  threads: `https://www.threads.net/@${SOCIAL_HANDLE}`,
} as const;

const ENTRIES: Omit<SocialEntry, "href">[] = [
  { id: "x", Icon: IconX },
  { id: "facebook", Icon: IconFacebook },
  { id: "instagram", Icon: IconInstagram },
  { id: "threads", Icon: IconThreads },
];

export async function SocialMediaLinks() {
  const t = await getTranslations("Footer");

  const items: SocialEntry[] = ENTRIES.map((entry) => ({
    ...entry,
    href:
      (entry.id === "x"
        ? process.env.NEXT_PUBLIC_SOCIAL_X_URL
        : entry.id === "facebook"
          ? process.env.NEXT_PUBLIC_SOCIAL_FACEBOOK_URL
          : entry.id === "instagram"
            ? process.env.NEXT_PUBLIC_SOCIAL_INSTAGRAM_URL
            : process.env.NEXT_PUBLIC_SOCIAL_THREADS_URL) ||
      DEFAULT_HREFS[entry.id],
  }));

  return (
    <div className="flex flex-wrap items-center gap-2">
      <span className="sr-only">{t("social.label")}</span>
      <ul className="flex flex-wrap gap-2" role="list">
        {items.map(({ id, href, Icon }) => (
          <li key={id}>
            <a
              href={href}
              target="_blank"
              rel="noopener noreferrer"
              className="inline-flex size-10 items-center justify-center rounded-full border border-border bg-background text-foreground transition-colors hover:bg-muted hover:text-foreground"
              aria-label={t(`social.${id}`)}
            >
              <Icon className="size-5" />
            </a>
          </li>
        ))}
      </ul>
    </div>
  );
}
